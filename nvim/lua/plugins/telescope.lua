local telescope_features = require('telescope.builtin')
local telescope_themes = require('telescope.themes')
-- See `:help telescope.builtin`
vim.keymap.set('n', '<leader>?', telescope_features.oldfiles, { desc = '[?] Find recently opened files' })
vim.keymap.set('n', '<leader><space>', telescope_features.buffers, { desc = '[ ] Find existing buffers' })
vim.keymap.set('n', '<leader>/', function() -- You can pass additional configuration to telescope to change theme, layout, etc.
  telescope_features.current_buffer_fuzzy_find(telescope_themes.get_dropdown {
    winblend = 10,
    previewer = false,
  })
end, { desc = '[/] Fuzzily search in current buffer' })

vim.keymap.set('n', '<leader>af', telescope_features.find_files, { desc = '[A]ll [F]iles' })
vim.keymap.set('n', '<leader>sf', telescope_features.git_files, { desc = '[S]earch [F]iles' })
vim.keymap.set('n', '<leader>sh', telescope_features.help_tags, { desc = '[S]earch [H]elp' })
vim.keymap.set('n', '<leader>sw', telescope_features.grep_string, { desc = '[S]earch current [W]ord' })
vim.keymap.set('n', '<leader>sg', telescope_features.live_grep, { desc = '[S]earch by [G]rep' })
vim.keymap.set('n', '<leader>sd', telescope_features.diagnostics, { desc = '[S]earch [D]iagnostics' })
